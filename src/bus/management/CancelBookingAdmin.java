    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
               int index =jTable1.getSelectedRow();
        TableModel model=jTable1.getModel();
        int id= (int)model.getValueAt(index, 0);
        System.out.println(id);
        String busno=(String) model.getValueAt(index,3);
        System.out.println(busno);
        String seat=(String) model.getValueAt(index, 4);
        System.out.println(seat);
        int seatnum=Integer.parseInt(seat);
        try
        {
            Class.forName("com.mysql.jdbc.Driver");
            String databaseURL = "jdbc:mysql://localhost:3306/busm";
            Connection con = DriverManager.getConnection(databaseURL, "root", "");
            Statement stat = con.createStatement();    
            String Query ="delete from bus_booking where booking_id='"+id+"'";
            stat.executeUpdate(Query);
            String QueryU="select * from bus_details where bus_no='"+busno+"'";
            ResultSet rs=stat.executeQuery(QueryU);
            while(rs.next())
            {
                String total_seats=rs.getString("total_seat");
                int seats=Integer.parseInt(total_seats);
                int update_seat=seats+seatnum;
                
                String UpdateQuery="update bus_details set total_seat='"+update_seat+"' where bus_no='"+busno+"'";
                stat.executeUpdate(UpdateQuery);
                infoMessage("Booking Cancelled Successfully", "Success!!");
                dispose(); 
            }

        }
        catch(Exception e)
        {
        }
    }//GEN-LAST:event_jTable1MouseClicked

